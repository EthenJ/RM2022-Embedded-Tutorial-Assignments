uint8_t m[256] = {0};
uint8_t output[16] = {0};
int i = 0;
uint8_t r[3] = {'a', 'n', 's'};

void solve()
{
  // static volatile int c1_11 = 0;
  int a = 0, b = 0, c = 0, c_l = 0, s = 0, d = 1;

  for (int j = 0; j < 16; j++)
  {
    output[j] = 0;
  }

  for (int j = i; j >= 0; j--)
  {
    if (m[j] == '!')
    {
      c = a + b;
      c_l = 0;
      for (int e = c; e; e /= 10)
        c_l++;
      output[0] = '!';
      output[c_l + 1] = '#';
      for (int j = c_l; j > 0; j--)
      {
        output[j] = c % 10 + '0';
        c /= 10;
      }
      for (int j = c_l + 2; j < 16; j++)
      {
        output[j] = '\0';
      }
      // c1_11 = c_l;
      HAL_UART_Transmit(&huart1, output, c_l + 2, 50);
      for (int j = 0; j < 256; j++)
      {
        m[j] = '\0';
      }
      return;
    }
    else if (m[j] == '#')
    {
      s = 1, d = 1;
    }
    else if (m[j] == ':')
    {
      s = 2, d = 1;
    }
    else if (s == 1)
    {
      a += (m[j] - '0') * d;
      d *= 10;
    }
    else if (s == 2)
    {
      b += (m[j] - '0') * d;
      d *= 10;
    }
  }
}

void store(uint8_t input[1])
{
  if (input[0] == '!')
  {
    for (int j = 0; j < 256; j++)
    {
      m[j] = '\0';
    }
    m[i] = '!';
  }
  m[i] = input[0];
  if (m[i] == '#')
  {
    HAL_UART_Transmit(&huart1, r, 3, 10);
    solve();
  }
  i++;
  input[0] = '\0';
}

